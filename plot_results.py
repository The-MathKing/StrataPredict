import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# Set publication-quality formatting (Academic Style)
plt.style.use('seaborn-v0_8-paper')
plt.rcParams.update({
    'font.family': 'serif',
    'font.size': 12,
    'axes.labelsize': 14,
    'axes.titlesize': 16,
    'legend.fontsize': 12,
    'xtick.labelsize': 10,
    'ytick.labelsize': 10,
    'lines.linewidth': 2.5,
    'figure.dpi': 300
})

def generate_academic_plots(csv_file):
    print(f"Loading data from {csv_file}...")
    df = pd.read_csv(csv_file)
    
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 6))
    
    # Colors: Crimson for Custom Model, Deep Blue for Baseline
    color_cw = '#c0392b'
    color_gin = '#2980b9'
    
    # ---------------------------------------------------
    # Plot 1: Test Accuracy Convergence
    # ---------------------------------------------------
    ax1.plot(df['Epoch'], df['CW_Test_Acc_Mean'], label='DynamicCW-Net (Ours)', color=color_cw)
    ax1.plot(df['Epoch'], df['GIN_Test_Acc_Mean'], label='GIN Baseline (1-WL)', color=color_gin, linestyle='--')
    
    ax1.set_title('Test Accuracy Convergence on NCI1 (5-Fold Mean)')
    ax1.set_xlabel('Training Epoch')
    ax1.set_ylabel('Test Accuracy (%)')
    ax1.legend(loc='lower right')
    ax1.grid(True, linestyle=':', alpha=0.7)
    
    # Highlight the final gap
    final_epoch = df['Epoch'].iloc[-1]
    final_cw_acc = df['CW_Test_Acc_Mean'].iloc[-1]
    final_gin_acc = df['GIN_Test_Acc_Mean'].iloc[-1]
    
    # Dynamically pad the top of the y-axis so the annotation isn't clipped
    y_min, y_max = ax1.get_ylim()
    ax1.set_ylim(y_min, max(y_max, final_cw_acc + 3.0))

    # Adjusted the xytext offset to ensure it fits well within the new bounds
    ax1.annotate(f'+{final_cw_acc - final_gin_acc:.2f}%', 
                 xy=(final_epoch, final_cw_acc), 
                 xytext=(final_epoch - 40, final_cw_acc + 1.2),
                 arrowprops=dict(facecolor='black', arrowstyle='->', lw=1.5),
                 fontsize=12, fontweight='bold', color=color_cw)

    # ---------------------------------------------------
    # Plot 2: Training Loss Optimization
    # ---------------------------------------------------
    ax2.plot(df['Epoch'], df['CW_Train_Loss_Mean'], label='DynamicCW-Net (Ours)', color=color_cw)
    ax2.plot(df['Epoch'], df['GIN_Train_Loss_Mean'], label='GIN Baseline (1-WL)', color=color_gin, linestyle='--')
    
    ax2.set_title('Training Loss Trajectory')
    ax2.set_xlabel('Training Epoch')
    ax2.set_ylabel('Cross-Entropy Loss')
    ax2.legend(loc='upper right')
    ax2.grid(True, linestyle=':', alpha=0.7)

    plt.tight_layout()
    
    # Save as high-res PDF and PNG for the presentation board
    plt.savefig('NCI1_Convergence_Analysis.pdf', format='pdf', bbox_inches='tight')
    plt.savefig('NCI1_Convergence_Analysis.png', format='png', bbox_inches='tight', dpi=300)
    print("Successfully generated NCI1_Convergence_Analysis.pdf and .png")

if __name__ == "__main__":
    # Assuming the CSV generated by the crucible is in the same directory
    generate_academic_plots('epoch_loss_curves.csv')
